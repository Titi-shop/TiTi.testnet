import { NextResponse } from "next/server";
import { kv } from "@vercel/kv";

// ----------------------------
// üîπ GET: L·∫•y danh s√°ch ƒë∆°n
// ----------------------------
export async function GET() {
  try {
    const data = await kv.get("orders");
    const orders = Array.isArray(data) ? data : JSON.parse(data || "[]");
    return NextResponse.json(orders);
  } catch (err) {
    console.error("‚ùå GET /orders:", err);
    return NextResponse.json([], { status: 500 });
  }
}

// ----------------------------
// üîπ POST: T·∫°o ƒë∆°n m·ªõi
// ----------------------------
export async function POST(req: Request) {
  try {
    const order = await req.json();
    const stored = await kv.get("orders");
    const orders = Array.isArray(stored)
      ? stored
      : JSON.parse(stored || "[]");

    const newOrder = {
      id: order.id ?? Date.now(),
      buyer: order.buyer || "unknown",
      items: order.items ?? [],
      total: order.total ?? 0,
      status: order.status ?? "Ch·ªù x√°c nh·∫≠n",
      note: order.note ?? "",
      createdAt: new Date().toISOString(),
    };

    orders.unshift(newOrder);
    await kv.set("orders", JSON.stringify(orders));

    return NextResponse.json({ success: true, order: newOrder });
  } catch (err) {
    console.error("‚ùå POST /orders:", err);
    return NextResponse.json({ success: false }, { status: 500 });
  }
}

// ----------------------------
// üîπ PUT: C·∫≠p nh·∫≠t tr·∫°ng th√°i ƒë∆°n
// ----------------------------
export async function PUT(req: Request) {
  try {
    const { id, status } = await req.json();
    const stored = await kv.get("orders");
    const orders = Array.isArray(stored)
      ? stored
      : JSON.parse(stored || "[]");

    const index = orders.findIndex((o) => String(o.id) === String(id));
    if (index === -1)
      return NextResponse.json(
        { success: false, message: "Kh√¥ng t√¨m th·∫•y ƒë∆°n h√†ng" },
        { status: 404 }
      );

    orders[index] = {
      ...orders[index],
      status,
      updatedAt: new Date().toISOString(),
    };

    await kv.set("orders", JSON.stringify(orders));
    return NextResponse.json({ success: true, order: orders[index] });
  } catch (err) {
    console.error("‚ùå PUT /orders:", err);
    return NextResponse.json({ success: false }, { status: 500 });
  }
}
