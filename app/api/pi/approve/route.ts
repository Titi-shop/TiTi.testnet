import { NextResponse } from "next/server";

/**
 * ‚úÖ API duy·ªát (approve) thanh to√°n Pi Network
 * - D√πng API_KEY th·∫≠t (Pi Developer Dashboard)
 * - Ho·∫°t ƒë·ªông c·∫£ tr√™n SANDBOX (testnet) v√† MAINNET th·∫≠t
 */
export async function POST(req: Request) {
  try {
    const { paymentId } = await req.json();

    if (!paymentId) {
      return NextResponse.json(
        { success: false, message: "Thi·∫øu paymentId" },
        { status: 400 }
      );
    }

    const API_KEY = process.env.PI_API_KEY;
    const isSandbox =
      process.env.PI_ENV === "sandbox" ||
      process.env.PI_API_URL?.includes("/sandbox");

    const API_BASE = isSandbox
      ? "https://api.minepi.com/v2/sandbox/payments"
      : "https://api.minepi.com/v2/payments";

    const endpoint = `${API_BASE}/${paymentId}/approve`;

    if (!API_KEY) {
      console.error("‚ùå Thi·∫øu PI_API_KEY trong .env");
      return NextResponse.json(
        { success: false, message: "Thi·∫øu Pi API Key" },
        { status: 500 }
      );
    }

    console.log(`‚è≥ [Pi APPROVE] Giao d·ªãch: ${paymentId} (${isSandbox ? "SANDBOX" : "MAINNET"})`);

    const res = await fetch(endpoint, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Key ${API_KEY}`,
      },
    });

    const text = await res.text();
    let result;
    try {
      result = JSON.parse(text);
    } catch {
      result = { raw: text };
    }

    console.log("‚úÖ [Pi APPROVE RESULT]:", res.status, result);

    // N·∫øu sai key ho·∫∑c domain ch∆∞a ƒëƒÉng k√Ω
    if (res.status === 401) {
      console.error("‚ùå API Key sai ho·∫∑c domain ch∆∞a ƒëƒÉng k√Ω tr√™n Pi Developer Portal!");
    }

    return NextResponse.json(result, {
      status: res.status,
      headers: { "Access-Control-Allow-Origin": "*" },
    });
  } catch (err: any) {
    console.error("üí• [Pi APPROVE ERROR]:", err);
    return NextResponse.json(
      { success: false, message: err.message || "L·ªói x√°c nh·∫≠n giao d·ªãch" },
      { status: 500 }
    );
  }
}
