export async function POST(req: Request) {
  try {
    const { paymentId } = await req.json();
    if (!paymentId) {
      return NextResponse.json({ success: false, message: "Thiáº¿u paymentId" }, { status: 400 });
    }

    const API_KEY = process.env.PI_API_KEY;
    const isSandbox = process.env.PI_ENV === "sandbox";
    const API_BASE = isSandbox
      ? "https://api.minepi.com/v2/sandbox/payments"
      : "https://api.minepi.com/v2/payments";
    const endpoint = `${API_BASE}/${paymentId}/approve`;

    const res = await fetch(endpoint, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Key ${API_KEY}`,
      },
    });

    const result = await res.json();
    console.log("âœ… [Pi APPROVE RESULT]:", result);

    return NextResponse.json(result, { status: res.status });
  } catch (err: any) {
    console.error("ðŸ’¥ [Pi APPROVE ERROR]:", err);
    return NextResponse.json({ success: false, message: err.message }, { status: 500 });
  }
}
