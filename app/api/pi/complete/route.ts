import { NextResponse } from "next/server";

/**
 * ‚úÖ API x√°c nh·∫≠n ho√†n t·∫•t thanh to√°n Pi Network
 * - G·ª≠i POST ƒë·∫øn /{paymentId}/complete
 * - D√πng API_KEY th·∫≠t (kh√¥ng sandbox)
 */
export async function POST(req: Request) {
  try {
    const { paymentId, txid } = await req.json();

    if (!paymentId) {
      return NextResponse.json(
        { success: false, message: "Thi·∫øu paymentId" },
        { status: 400 }
      );
    }

    const API_KEY = process.env.PI_API_KEY;
    const isSandbox =
      process.env.PI_ENV === "sandbox" ||
      process.env.PI_API_URL?.includes("/sandbox");

    const API_BASE = isSandbox
      ? "https://api.minepi.com/v2/sandbox/payments"
      : "https://api.minepi.com/v2/payments";

    const endpoint = `${API_BASE}/${paymentId}/complete`;

    console.log(`‚è≥ [Pi COMPLETE] X√°c nh·∫≠n ${paymentId} (${isSandbox ? "SANDBOX" : "MAINNET"})`);

    const res = await fetch(endpoint, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Key ${API_KEY}`,
      },
      body: JSON.stringify({ txid }),
    });

    const resultText = await res.text();
    let result;
    try {
      result = JSON.parse(resultText);
    } catch {
      result = { raw: resultText };
    }

    console.log("‚úÖ [Pi COMPLETE RESULT]:", res.status, result);

    return NextResponse.json(result, {
      status: res.status,
      headers: { "Access-Control-Allow-Origin": "*" },
    });
  } catch (err: any) {
    console.error("üí• [Pi COMPLETE ERROR]:", err);
    return NextResponse.json(
      { success: false, message: err.message || "L·ªói x√°c nh·∫≠n thanh to√°n" },
      { status: 500 }
    );
  }
}
