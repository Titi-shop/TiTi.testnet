import { NextResponse } from "next/server";
import { del, put, list } from "@vercel/blob";

// ==================================
// üß© ƒê·ªçc s·∫£n ph·∫©m t·ª´ Blob (lu√¥n l·∫•y b·∫£n m·ªõi nh·∫•t)
// ==================================
async function readProducts() {
  try {
    const { blobs } = await list();
    const file = blobs.find((b) => b.pathname === "products.json");

    if (!file) {
      console.warn("‚ö†Ô∏è Kh√¥ng t√¨m th·∫•y file products.json ‚Äî tr·∫£ m·∫£ng r·ªóng");
      return [];
    }

    const res = await fetch(file.url, { cache: "no-store" });
    return await res.json();
  } catch (err) {
    console.error("‚ùå L·ªói ƒë·ªçc products.json:", err);
    return [];
  }
}

// ==================================
// üß© Ghi s·∫£n ph·∫©m m·ªõi v√†o Blob
// ==================================
async function writeProducts(products: any[]) {
  try {
    const data = JSON.stringify(products, null, 2);
    const { blobs } = await list();
    const old = blobs.find((b) => b.pathname === "products.json");
    if (old) await del("products.json");

    await put("products.json", data, {
      access: "public",
      addRandomSuffix: false,
    });

    console.log("‚úÖ Ghi th√†nh c√¥ng products.json:", products.length);
  } catch (err) {
    console.error("‚ùå Ghi th·∫•t b·∫°i:", err);
  }
}

// ==============================
// üîπ GET ‚Äî L·∫•y danh s√°ch s·∫£n ph·∫©m
// ==============================
export async function GET() {
  const products = await readProducts();
  return NextResponse.json(products);
}

// ==============================
// üîπ POST ‚Äî Th√™m s·∫£n ph·∫©m m·ªõi
// ==============================
export async function POST(req: Request) {
  try {
    const body = await req.json();
    const { name, price, description, images } = body;

    if (!name || !price)
      return NextResponse.json(
        { success: false, message: "Thi·∫øu t√™n ho·∫∑c gi√° s·∫£n ph·∫©m" },
        { status: 400 }
      );

    const products = await readProducts();

    const newProduct = {
      id: Date.now(),
      name,
      price,
      description: description || "",
      images: images || [],
      createdAt: new Date().toISOString(),
    };

    products.unshift(newProduct);
    await writeProducts(products);

    return NextResponse.json({ success: true, product: newProduct });
  } catch (err) {
    console.error("‚ùå POST error:", err);
    return NextResponse.json(
      { success: false, message: "L·ªói khi th√™m s·∫£n ph·∫©m" },
      { status: 500 }
    );
  }
}

// ==============================
// üîπ DELETE ‚Äî X√≥a s·∫£n ph·∫©m
// ==============================
export async function DELETE(req: Request) {
  try {
    const { searchParams } = new URL(req.url);
    const id = Number(searchParams.get("id"));
    if (!id)
      return NextResponse.json(
        { success: false, message: "Thi·∫øu ID" },
        { status: 400 }
      );

    const products = await readProducts();
    const updated = products.filter((p) => p.id !== id);
    await writeProducts(updated);

    return NextResponse.json({ success: true });
  } catch (err) {
    console.error("‚ùå DELETE error:", err);
    return NextResponse.json(
      { success: false, message: "L·ªói khi x√≥a s·∫£n ph·∫©m" },
      { status: 500 }
    );
  }
}

// ==============================
// üîπ PUT ‚Äî C·∫≠p nh·∫≠t s·∫£n ph·∫©m
// ==============================
export async function PUT(req: Request) {
  try {
    const formData = await req.formData();

    const id = Number(formData.get("id"));
    const name = formData.get("name") as string;
    const price = Number(formData.get("price"));
    const description = formData.get("description") as string;
    const images = formData.getAll("images") as string[];

    if (!id || !name || !price) {
      return NextResponse.json(
        { success: false, message: "Thi·∫øu d·ªØ li·ªáu s·∫£n ph·∫©m" },
        { status: 400 }
      );
    }

    const products = await readProducts();
    const index = products.findIndex((p: any) => p.id === id);

    if (index === -1) {
      return NextResponse.json(
        { success: false, message: "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m ƒë·ªÉ c·∫≠p nh·∫≠t" },
        { status: 404 }
      );
    }

    const updatedProduct = {
      ...products[index],
      name,
      price,
      description,
      images,
      updatedAt: new Date().toISOString(),
    };

    products[index] = updatedProduct;
    await writeProducts(products);

    console.log("‚úÖ ƒê√£ c·∫≠p nh·∫≠t s·∫£n ph·∫©m:", updatedProduct);
    return NextResponse.json({ success: true, product: updatedProduct });
  } catch (err) {
    console.error("‚ùå PUT error:", err);
    return NextResponse.json(
      { success: false, message: "Kh√¥ng th·ªÉ c·∫≠p nh·∫≠t s·∫£n ph·∫©m" },
      { status: 500 }
    );
  }
}
