import { NextResponse } from "next/server";
import { put, list, del } from "@vercel/blob";

// T√™n file l∆∞u danh s√°ch s·∫£n ph·∫©m trong Blob
const PRODUCTS_BLOB = "products.json";

// ==============================
// üîπ H√†m h·ªó tr·ª£: ƒê·ªçc danh s√°ch s·∫£n ph·∫©m t·ª´ Blob
// ==============================
async function readProducts() {
  try {
    const blobs = await list();
    const file = blobs.blobs.find((b) => b.pathname === PRODUCTS_BLOB);
    if (!file) return [];
    const res = await fetch(file.url);
    const data = await res.json();
    return Array.isArray(data) ? data : [];
  } catch (err) {
    console.error("‚ùå L·ªói ƒë·ªçc Blob:", err);
    return [];
  }
}

// ==============================
// üîπ Ghi l·∫°i danh s√°ch v√†o Blob
// ==============================
async function writeProducts(products: any[]) {
  try {
    await put(PRODUCTS_BLOB, JSON.stringify(products, null, 2), {
      access: "public",
      addRandomSuffix: false, // ghi ƒë√® file c≈©
    });
  } catch (err) {
    console.error("‚ùå L·ªói ghi Blob:", err);
  }
}

// ==============================
// üîπ GET ‚Äî L·∫•y danh s√°ch s·∫£n ph·∫©m
// ==============================
export async function GET() {
  const products = await readProducts();
  return NextResponse.json(products);
}

// ==============================
// üîπ POST ‚Äî Th√™m s·∫£n ph·∫©m m·ªõi
// ==============================
export async function POST(req: Request) {
  try {
    const body = await req.json();
    const { name, price, description, images } = body;

    if (!name || !price) {
      return NextResponse.json(
        { success: false, message: "Thi·∫øu t√™n ho·∫∑c gi√° s·∫£n ph·∫©m" },
        { status: 400 }
      );
    }

    const products = await readProducts();
    const newProduct = {
      id: Date.now(),
      name,
      price,
      description,
      images: images || [],
      createdAt: new Date().toISOString(),
    };

    products.unshift(newProduct);
    await writeProducts(products);

    return NextResponse.json({ success: true, product: newProduct });
  } catch (error) {
    console.error("‚ùå L·ªói POST:", error);
    return NextResponse.json(
      { success: false, message: "L·ªói khi th√™m s·∫£n ph·∫©m" },
      { status: 500 }
    );
  }
}

// ==============================
// üîπ PUT ‚Äî C·∫≠p nh·∫≠t s·∫£n ph·∫©m
// ==============================
export async function PUT(req: Request) {
  try {
    const formData = await req.formData();
    const id = Number(formData.get("id"));
    if (!id)
      return NextResponse.json(
        { success: false, message: "Thi·∫øu ID s·∫£n ph·∫©m" },
        { status: 400 }
      );

    const products = await readProducts();
    const index = products.findIndex((p) => p.id === id);
    if (index === -1)
      return NextResponse.json(
        { success: false, message: "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m" },
        { status: 404 }
      );

    const name = formData.get("name") as string;
    const price = formData.get("price") as string;
    const description = formData.get("description") as string;
    const imagesData = formData.getAll("images");

    // ‚úÖ X·ª≠ l√Ω ·∫£nh: URL c≈© ho·∫∑c File m·ªõi
    let uploadedUrls: string[] = [];
    for (const item of imagesData) {
      if (typeof item === "string") {
        uploadedUrls.push(item);
      } else if (item instanceof File) {
        const res = await fetch("/api/upload", {
          method: "POST",
          headers: {
            "Content-Type": item.type,
            "x-filename": item.name,
          },
          body: item,
        });
        const data = await res.json();
        if (data?.url) uploadedUrls.push(data.url);
      }
    }

    if (uploadedUrls.length === 0) uploadedUrls = products[index].images;

    const updatedProduct = {
      ...products[index],
      name,
      price,
      description,
      images: uploadedUrls,
      updatedAt: new Date().toISOString(),
    };

    products[index] = updatedProduct;
    await writeProducts(products);

    return NextResponse.json({ success: true, product: updatedProduct });
  } catch (error) {
    console.error("‚ùå L·ªói PUT:", error);
    return NextResponse.json(
      { success: false, message: "C·∫≠p nh·∫≠t th·∫•t b·∫°i" },
      { status: 500 }
    );
  }
}

// ==============================
// üîπ DELETE ‚Äî X√≥a s·∫£n ph·∫©m
// ==============================
export async function DELETE(req: Request) {
  try {
    const { searchParams } = new URL(req.url);
    const id = Number(searchParams.get("id"));
    if (!id)
      return NextResponse.json(
        { success: false, message: "Thi·∫øu ID s·∫£n ph·∫©m" },
        { status: 400 }
      );

    let products = await readProducts();
    const before = products.length;
    products = products.filter((p) => p.id !== id);
    if (products.length === before)
      return NextResponse.json(
        { success: false, message: "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m" },
        { status: 404 }
      );

    await writeProducts(products);
    return NextResponse.json({ success: true });
  } catch (error) {
    console.error("‚ùå L·ªói DELETE:", error);
    return NextResponse.json(
      { success: false, message: "X√≥a s·∫£n ph·∫©m th·∫•t b·∫°i" },
      { status: 500 }
    );
  }
}
