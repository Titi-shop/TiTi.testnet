import { NextResponse } from "next/server";
import { del, put, list, get } from "@vercel/blob";  // <--- Th√™m d√≤ng n√†y

// üß© H√†m ghi s·∫£n ph·∫©m v√†o Blob
async function writeProducts(products: any[]) {
  try {
    // üß© B∆∞·ªõc 1: Ghi t·∫°m file m·ªõi
    const tempFileName = "products-temp.json";
    await put(tempFileName, JSON.stringify(products, null, 2), {
      access: "public",
      addRandomSuffix: false,
    });

    // üß© B∆∞·ªõc 2: X√≥a file c≈© n·∫øu t·ªìn t·∫°i
    try {
      await del("products.json");
    } catch (err) {
      console.warn("‚ö†Ô∏è Kh√¥ng t√¨m th·∫•y products.json c≈©, b·ªè qua xo√°.");
    }

    // üß© B∆∞·ªõc 3: Ghi l·∫°i file ch√≠nh th·ª©c
    await put("products.json", JSON.stringify(products, null, 2), {
      access: "public",
      addRandomSuffix: false,
    });

    console.log("‚úÖ Ghi products.json th√†nh c√¥ng:", products.length);
  } catch (err) {
    console.error("‚ùå L·ªói ghi products.json:", err);
  }
}

// ==================================
// üóÇ B·ªô nh·ªõ t·∫°m (RAM)
// ==================================
let products: any[] = [];

// ‚úÖ ƒê·ªçc d·ªØ li·ªáu t·ª´ Blob khi kh·ªüi ƒë·ªông
(async () => {
  try {
    const existing = await get("products.json");
    if (existing?.blob) {
      const text = await existing.blob.text();
      products = JSON.parse(text);
      console.log("üì¶ ƒê√£ load", products.length, "s·∫£n ph·∫©m t·ª´ Blob");
    }
  } catch (err) {
    console.warn("‚ö†Ô∏è Ch∆∞a c√≥ products.json trong Blob");
  }
})();

// ==============================
// üîπ GET ‚Äî L·∫•y danh s√°ch s·∫£n ph·∫©m
// ==============================
export async function GET() {
  return NextResponse.json(products);
}

// ==============================
// üîπ POST ‚Äî Th√™m s·∫£n ph·∫©m m·ªõi
// ==============================
export async function POST(req: Request) {
  try {
    const body = await req.json();
    const { name, price, description, images } = body;

    if (!name || !price)
      return NextResponse.json({ success: false, message: "Thi·∫øu t√™n ho·∫∑c gi√°" }, { status: 400 });

    const newProduct = {
      id: Date.now(),
      name,
      price,
      description,
      images: images || [],
      createdAt: new Date().toISOString(),
    };

    products.unshift(newProduct);

    // üß© Ghi l·∫°i Blob JSON
    await writeProducts(products);

    return NextResponse.json({ success: true, product: newProduct });
  } catch (error) {
    console.error("‚ùå POST Error:", error);
    return NextResponse.json({ success: false, message: "L·ªói khi th√™m" }, { status: 500 });
  }
}

// ==============================
// üîπ PUT ‚Äî C·∫≠p nh·∫≠t s·∫£n ph·∫©m
// ==============================
export async function PUT(req: Request) {
  try {
    const formData = await req.formData();
    const id = Number(formData.get("id"));
    const name = formData.get("name") as string;
    const price = formData.get("price") as string;
    const description = formData.get("description") as string;
    const images = formData.getAll("images") as string[];

    const index = products.findIndex((p) => p.id === id);
    if (index === -1)
      return NextResponse.json({ success: false, message: "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m" }, { status: 404 });

    const updated = {
      ...products[index],
      name,
      price,
      description,
      images,
      updatedAt: new Date().toISOString(),
    };

    products[index] = updated;
    await writeProducts(products);

    return NextResponse.json({ success: true, product: updated });
  } catch (err) {
    console.error("‚ùå PUT Error:", err);
    return NextResponse.json({ success: false, message: "C·∫≠p nh·∫≠t th·∫•t b·∫°i" }, { status: 500 });
  }
}

// ==============================
// üîπ DELETE ‚Äî X√≥a s·∫£n ph·∫©m
// ==============================
export async function DELETE(req: Request) {
  try {
    const { searchParams } = new URL(req.url);
    const id = Number(searchParams.get("id"));
    if (!id)
      return NextResponse.json({ success: false, message: "Thi·∫øu ID s·∫£n ph·∫©m" }, { status: 400 });

    const before = products.length;
    products = products.filter((p) => p.id !== id);

    if (before === products.length)
      return NextResponse.json({ success: false, message: "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m" }, { status: 404 });

    await writeProducts(products);
    return NextResponse.json({ success: true });
  } catch (err) {
    console.error("‚ùå DELETE Error:", err);
    return NextResponse.json({ success: false, message: "X√≥a th·∫•t b·∫°i" }, { status: 500 });
  }
}
