import { NextResponse } from "next/server";
import { promises as fs } from "fs";
import path from "path";

const dataFile = path.join(process.cwd(), "data", "products.json");

// ‚úÖ ƒê·∫£m b·∫£o th∆∞ m·ª•c /data t·ªìn t·∫°i
async function ensureDataFile() {
  const dir = path.dirname(dataFile);
  try {
    await fs.mkdir(dir, { recursive: true });
    await fs.access(dataFile);
  } catch {
    await fs.writeFile(dataFile, "[]", "utf-8");
  }
}

// ‚úÖ ƒê·ªçc danh s√°ch s·∫£n ph·∫©m
async function readProducts() {
  await ensureDataFile();
  const data = await fs.readFile(dataFile, "utf-8");
  return JSON.parse(data || "[]");
}

// ‚úÖ Ghi danh s√°ch s·∫£n ph·∫©m
async function writeProducts(products: any[]) {
  await ensureDataFile();
  await fs.writeFile(dataFile, JSON.stringify(products, null, 2), "utf-8");
}

// ==============================
// üîπ GET ‚Äî L·∫•y danh s√°ch s·∫£n ph·∫©m
// ==============================
export async function GET() {
  const products = await readProducts();
  return NextResponse.json(products);
}

// ==============================
// üîπ POST ‚Äî Th√™m s·∫£n ph·∫©m m·ªõi
// ==============================
export async function POST(req: Request) {
  try {
    const body = await req.json();
    const { name, price, description, images } = body;

    if (!name || !price) {
      return NextResponse.json(
        { success: false, message: "Thi·∫øu t√™n ho·∫∑c gi√° s·∫£n ph·∫©m" },
        { status: 400 }
      );
    }

    const products = await readProducts();
    const newProduct = {
      id: Date.now(),
      name,
      price,
      description,
      images: images || [],
      createdAt: new Date().toISOString(),
    };

    products.unshift(newProduct);
    await writeProducts(products);

    return NextResponse.json({ success: true, product: newProduct });
  } catch (error) {
    console.error("‚ùå L·ªói POST:", error);
    return NextResponse.json(
      { success: false, message: "L·ªói khi th√™m s·∫£n ph·∫©m" },
      { status: 500 }
    );
  }
}

// ==============================
// üîπ PUT ‚Äî C·∫≠p nh·∫≠t s·∫£n ph·∫©m
// ==============================
export async function PUT(req: Request) {
  try {
    const formData = await req.formData();
    const id = Number(formData.get("id"));

    if (!id) {
      return NextResponse.json(
        { success: false, message: "Thi·∫øu ID s·∫£n ph·∫©m" },
        { status: 400 }
      );
    }

    const products = await readProducts();
    const index = products.findIndex((p: any) => p.id === id);
    if (index === -1)
      return NextResponse.json(
        { success: false, message: "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m" },
        { status: 404 }
      );

    const name = formData.get("name") as string;
    const price = formData.get("price") as string;
    const description = formData.get("description") as string;
    const imageValues = formData.getAll("images").map((i) => i.toString());

    // ‚úÖ C·∫≠p nh·∫≠t
    products[index] = {
      ...products[index],
      name,
      price,
      description,
      images: imageValues,
      updatedAt: new Date().toISOString(),
    };

    await writeProducts(products);

    return NextResponse.json({ success: true, product: products[index] });
  } catch (error) {
    console.error("‚ùå L·ªói PUT:", error);
    return NextResponse.json(
      { success: false, message: "C·∫≠p nh·∫≠t th·∫•t b·∫°i" },
      { status: 500 }
    );
  }
}

// ==============================
// üîπ DELETE ‚Äî X√≥a s·∫£n ph·∫©m
// ==============================
export async function DELETE(req: Request) {
  try {
    const { searchParams } = new URL(req.url);
    const id = Number(searchParams.get("id"));
    if (!id)
      return NextResponse.json(
        { success: false, message: "Thi·∫øu ID s·∫£n ph·∫©m" },
        { status: 400 }
      );

    const products = await readProducts();
    const filtered = products.filter((p: any) => p.id !== id);
    await writeProducts(filtered);

    return NextResponse.json({ success: true });
  } catch (error) {
    console.error("‚ùå L·ªói DELETE:", error);
    return NextResponse.json(
      { success: false, message: "X√≥a s·∫£n ph·∫©m th·∫•t b·∫°i" },
      { status: 500 }
    );
  }
}
