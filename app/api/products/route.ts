import { NextResponse } from "next/server";
import { put } from "@vercel/blob";

// B·ªô nh·ªõ t·∫°m (RAM) l∆∞u s·∫£n ph·∫©m
let products: any[] = [];

// ==============================
// üîπ GET ‚Äî L·∫•y danh s√°ch s·∫£n ph·∫©m
// ==============================
export async function GET() {
  return NextResponse.json(products);
}

// ==============================
// üîπ POST ‚Äî Th√™m s·∫£n ph·∫©m m·ªõi
// ==============================
export async function POST(req: Request) {
  try {
    const formData = await req.formData();
    const name = formData.get("name") as string;
    const price = formData.get("price") as string;
    const description = formData.get("description") as string;
    const images = formData.getAll("images") as File[];

    if (!name || !price) {
      return NextResponse.json(
        { success: false, message: "Thi·∫øu th√¥ng tin s·∫£n ph·∫©m" },
        { status: 400 }
      );
    }

    // ‚úÖ Upload ·∫£nh l√™n Vercel Blob
    const imageUrls: string[] = [];
    for (const img of images) {
      const buffer = Buffer.from(await img.arrayBuffer());
      const blob = await put(`uploads/${Date.now()}-${img.name}`, buffer, {
        access: "public",
        contentType: img.type,
      });
      imageUrls.push(blob.url);
    }

    // ‚úÖ L∆∞u s·∫£n ph·∫©m v√†o b·ªô nh·ªõ t·∫°m
    const newProduct = {
      id: Date.now(),
      name,
      price,
      description,
      images: imageUrls,
      createdAt: new Date().toISOString(),
    };

    products.unshift(newProduct);

    return NextResponse.json({ success: true, product: newProduct });
  } catch (error: any) {
    console.error("‚ùå L·ªói POST:", error);
    return NextResponse.json(
      { success: false, message: error.message || "L·ªói th√™m s·∫£n ph·∫©m" },
      { status: 500 }
    );
  }
}
