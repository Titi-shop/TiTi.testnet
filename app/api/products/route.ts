import { NextResponse } from "next/server";
import { del, put, list } from "@vercel/blob";

// ==================================
// üß© H√†m ghi s·∫£n ph·∫©m v√†o Blob
// ==================================
async function writeProducts(products: any[]) {
  try {
    const data = JSON.stringify(products, null, 2);

    // Ki·ªÉm tra xem file products.json c√≥ t·ªìn t·∫°i kh√¥ng
    const { blobs } = await list();
    const oldFile = blobs.find((b) => b.pathname === "products.json");

    if (oldFile) {
      console.log("üóë X√≥a file c≈©...");
      await del("products.json");
      // ƒê·ª£i 2 gi√¢y ƒë·ªÉ ƒë·∫£m b·∫£o x√≥a ho√†n t·∫•t tr√™n t·∫•t c·∫£ edge server
      await new Promise((r) => setTimeout(r, 2000));
    }

    // Ghi file m·ªõi (√©p ghi m·ªõi ho√†n to√†n)
    await put("products.json", data, {
      access: "public",
      addRandomSuffix: false,
    });

    console.log("‚úÖ Ghi products.json th√†nh c√¥ng:", products.length);
  } catch (err) {
    console.error("‚ùå L·ªói ghi products.json:", err);
  }
}

// ==================================
// üóÇ B·ªô nh·ªõ t·∫°m (RAM)
// ==================================
let products: any[] = [];

// ‚úÖ Load d·ªØ li·ªáu t·ª´ Blob khi server start
(async () => {
  try {
    const { blobs } = await list();
    const file = blobs.find((b) => b.pathname === "products.json");

    if (file) {
      const res = await fetch(file.url, { cache: "no-store" });
      products = await res.json();
      console.log("üì¶ ƒê√£ load", products.length, "s·∫£n ph·∫©m t·ª´ Blob");
    } else {
      console.warn("‚ö†Ô∏è Ch∆∞a c√≥ file products.json trong Blob");
    }
  } catch (err) {
    console.error("‚ùå L·ªói ƒë·ªçc products.json:", err);
  }
})();

// ==============================
// üîπ GET ‚Äî L·∫•y danh s√°ch s·∫£n ph·∫©m
// ==============================
export async function GET() {
  return NextResponse.json(products);
}

// ==============================
// üîπ POST ‚Äî Th√™m s·∫£n ph·∫©m m·ªõi
// ==============================
export async function POST(req: Request) {
  try {
    const body = await req.json();
    const { name, price, description, images } = body;

    if (!name || !price) {
      return NextResponse.json(
        { success: false, message: "Thi·∫øu t√™n ho·∫∑c gi√°" },
        { status: 400 }
      );
    }

    const newProduct = {
      id: Date.now(),
      name,
      price,
      description,
      images: images || [],
      createdAt: new Date().toISOString(),
    };

    products.unshift(newProduct);
    await writeProducts(products);

    return NextResponse.json({ success: true, product: newProduct });
  } catch (error) {
    console.error("‚ùå POST Error:", error);
    return NextResponse.json(
      { success: false, message: "L·ªói khi th√™m s·∫£n ph·∫©m" },
      { status: 500 }
    );
  }
}

// ==============================
// üîπ PUT ‚Äî C·∫≠p nh·∫≠t s·∫£n ph·∫©m
// ==============================
export async function PUT(req: Request) {
  try {
    const formData = await req.formData();
    const id = Number(formData.get("id"));
    const name = formData.get("name") as string;
    const price = formData.get("price") as string;
    const description = formData.get("description") as string;
    const images = formData.getAll("images") as string[];

    const index = products.findIndex((p) => p.id === id);
    if (index === -1)
      return NextResponse.json(
        { success: false, message: "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m" },
        { status: 404 }
      );

    const updated = {
      ...products[index],
      name,
      price,
      description,
      images,
      updatedAt: new Date().toISOString(),
    };

    products[index] = updated;
    await writeProducts(products);

    return NextResponse.json({ success: true, product: updated });
  } catch (err) {
    console.error("‚ùå PUT Error:", err);
    return NextResponse.json(
      { success: false, message: "C·∫≠p nh·∫≠t th·∫•t b·∫°i" },
      { status: 500 }
    );
  }
}

// ==============================
// üîπ DELETE ‚Äî X√≥a s·∫£n ph·∫©m
// ==============================
export async function DELETE(req: Request) {
  try {
    const { searchParams } = new URL(req.url);
    const id = Number(searchParams.get("id"));

    if (!id) {
      return NextResponse.json(
        { success: false, message: "Thi·∫øu ID s·∫£n ph·∫©m" },
        { status: 400 }
      );
    }

    const before = products.length;
    products = products.filter((p) => p.id !== id);

    if (before === products.length)
      return NextResponse.json(
        { success: false, message: "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m" },
        { status: 404 }
      );

    await writeProducts(products);
    return NextResponse.json({ success: true });
  } catch (err) {
    console.error("‚ùå DELETE Error:", err);
    return NextResponse.json(
      { success: false, message: "X√≥a th·∫•t b·∫°i" },
      { status: 500 }
    );
  }
}
