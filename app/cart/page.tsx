"use client";

import React from "react";
import Link from "next/link";
import { useCart } from "../context/CartContext";
import { useRouter } from "next/navigation";

export default function CartPage() {
  const { cart, removeFromCart, updateQty, clearCart } = useCart();
  const router = useRouter();

  // ‚úÖ Thanh to√°n 1 s·∫£n ph·∫©m qua Pi Testnet
  const handlePayOne = async (item: any) => {
    try {
      if (!window.Pi) {
        alert("‚ö†Ô∏è H√£y m·ªü trang n√†y trong Pi Browser ƒë·ªÉ thanh to√°n b·∫±ng Pi.");
        return;
      }

      const userInfo = JSON.parse(localStorage.getItem("user_info") || "{}");
      const buyer = userInfo.username || "guest_user";
      const orderId = Date.now(); // d√πng l√†m metadata

      const scopes = ["payments", "username", "wallet_address"];
      const auth = await window.Pi.authenticate(scopes, (res) => res);
      console.log("‚úÖ X√°c th·ª±c Pi:", auth);

      // ‚úÖ G·ªçi thanh to√°n th·∫≠t
      const payment = await window.Pi.createPayment(
        {
          amount: item.price * (item.quantity || 1),
          memo: `Thanh to√°n s·∫£n ph·∫©m ${item.name}`,
          metadata: {
            orderId,
            buyer,
            item: { id: item.id, name: item.name, price: item.price },
          },
        },
        {
          onReadyForServerApproval: async (paymentId) => {
            console.log("‚è≥ Approval:", paymentId);
            await fetch("/api/pi/approve", {
              method: "POST",
              headers: { "Content-Type": "application/json" },
              body: JSON.stringify({ paymentId, orderId }),
            });
          },
          onReadyForServerCompletion: async (paymentId, txid) => {
            console.log("‚úÖ Completed:", paymentId, txid);
            await fetch("/api/pi/complete", {
              method: "POST",
              headers: { "Content-Type": "application/json" },
              body: JSON.stringify({ paymentId, txid, metadata: { orderId } }),
            });
          },
          onCancel: () => alert("‚ùå B·∫°n ƒë√£ hu·ª∑ giao d·ªãch."),
          onError: (err) => console.error("üí• L·ªói SDK:", err),
        }
      );

      console.log("üí∞ K·∫øt qu·∫£:", payment);

      // ‚úÖ L∆∞u ƒë∆°n v√†o h·ªá th·ªëng
      const orderData = {
        id: orderId,
        buyer,
        total: item.price * (item.quantity || 1),
        items: [item],
        createdAt: new Date().toISOString(),
        status: "Ch·ªù x√°c nh·∫≠n (Pi ƒë√£ thanh to√°n)",
      };

      await fetch("/api/orders", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(orderData),
      });

      removeFromCart(item.id);
      alert(`üéâ Thanh to√°n th√†nh c√¥ng s·∫£n ph·∫©m: ${item.name}`);
      router.push("/customer/pending");
    } catch (error) {
      console.error("‚ùå L·ªói khi thanh to√°n:", error);
      alert("üí• L·ªói khi th·ª±c hi·ªán thanh to√°n Pi Testnet!");
    }
  };

  return (
    <main className="max-w-4xl mx-auto p-6">
      <h1 className="text-2xl font-bold mb-4">üõí Gi·ªè h√†ng</h1>

      {cart.length === 0 ? (
        <div className="text-center">
          <p>Gi·ªè h√†ng tr·ªëng.</p>
          <Link href="/" className="text-blue-600 hover:underline">
            Quay l·∫°i mua s·∫Øm
          </Link>
        </div>
      ) : (
        <div>
          <div className="space-y-4">
            {cart.map((it) => (
              <div
                key={it.id}
                className="flex flex-col sm:flex-row sm:items-center sm:justify-between gap-4 border p-3 rounded shadow-sm bg-white"
              >
                {/* H√¨nh ·∫£nh */}
                <div className="flex items-center gap-3 flex-1">
                  <div className="w-20 h-20 bg-gray-100 flex items-center justify-center overflow-hidden rounded">
                    {it.images?.[0] ? (
                      <img
                        src={it.images[0]}
                        className="w-full h-full object-cover"
                        alt={it.name}
                      />
                    ) : (
                      <span className="text-gray-400 text-sm">No image</span>
                    )}
                  </div>

                  <div className="flex-1">
                    <h3 className="font-semibold">{it.name}</h3>
                    <p className="text-yellow-600 font-bold">{it.price} Pi</p>
                    <p className="text-sm text-gray-600 line-clamp-2">
                      {it.description}
                    </p>
                  </div>
                </div>

                {/* Thao t√°c */}
                <div className="flex flex-col sm:flex-row items-center gap-2 sm:w-[200px] justify-end">
                  <input
                    type="number"
                    min={1}
                    value={it.quantity || 1}
                    onChange={(e) =>
                      updateQty(it.id, Math.max(1, Number(e.target.value)))
                    }
                    className="w-16 border p-1 rounded text-center"
                  />

                  <button
                    onClick={() => removeFromCart(it.id)}
                    className="bg-red-500 text-white px-3 py-1 rounded hover:bg-red-600"
                  >
                    X√≥a
                  </button>

                  {/* üî• Thanh to√°n th·∫≠t qua Pi Testnet */}
                  <button
                    onClick={() => handlePayOne(it)}
                    className="bg-purple-600 text-white px-3 py-1 rounded hover:bg-purple-700 flex items-center gap-1"
                  >
                    üí≥ Thanh to√°n (Pi)
                  </button>
                </div>
              </div>
            ))}
          </div>

          {/* ==== N√∫t x√≥a to√†n b·ªô ==== */}
          <div className="mt-6 flex justify-center">
            <button
              onClick={() => {
                clearCart();
                alert("üóëÔ∏è ƒê√£ xo√° to√†n b·ªô gi·ªè h√†ng!");
              }}
              className="bg-gray-400 text-white px-4 py-2 rounded hover:bg-gray-500"
            >
              Xo√° t·∫•t c·∫£
            </button>
          </div>
        </div>
      )}
    </main>
  );
}
