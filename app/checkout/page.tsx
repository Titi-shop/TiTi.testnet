"use client";

import { useEffect, useState } from "react";
import { useCart } from "../context/CartContext";
import { useRouter } from "next/navigation";

// Khai b√°o Pi SDK to√†n c·ª•c ƒë·ªÉ tr√°nh l·ªói build
declare global {
  interface Window {
    Pi?: any;
  }
}

export default function CheckoutPage() {
  const { cart, clearCart, total } = useCart();
  const [wallet, setWallet] = useState<number>(0);
  const [loading, setLoading] = useState(false);
  const [user, setUser] = useState("guest");
  const router = useRouter();

  // ‚úÖ Nh·∫≠n di·ªán user ƒë√£ ƒëƒÉng nh·∫≠p Pi (ƒë·ªìng b·ªô v·ªõi pilogin)
  useEffect(() => {
    try {
      const authData = localStorage.getItem("pi_user");
      const isLoggedIn = localStorage.getItem("titi_is_logged_in") === "true";
      const username = localStorage.getItem("titi_username");

      if (isLoggedIn && authData && username) {
        setUser(username);
      } else {
        setUser("guest");
      }
    } catch (err) {
      console.error("User load error:", err);
      setUser("guest");
    }
  }, []);

  // ‚úÖ L·∫•y v√≠ Pi (mock t·∫°m)
  useEffect(() => {
    const w = Number(localStorage.getItem("pi_wallet") ?? "1000");
    setWallet(w);
  }, []);

  // üí∞ Thanh to√°n qua Pi Wallet (Testnet)
  const handlePayWithPi = async () => {
    if (!window.Pi) {
      alert("‚ö†Ô∏è H√£y m·ªü trang n√†y trong Pi Browser ƒë·ªÉ thanh to√°n.");
      return;
    }
    if (cart.length === 0) return alert("üõí Gi·ªè h√†ng tr·ªëng.");
    if (user === "guest") {
      alert("‚ö†Ô∏è B·∫°n c·∫ßn ƒëƒÉng nh·∫≠p b·∫±ng Pi tr∆∞·ªõc khi thanh to√°n!");
      router.push("/pilogin");
      return;
    }

    setLoading(true);
    try {
      // ‚úÖ 1. X√°c th·ª±c ng∆∞·ªùi d√πng Pi
      window.Pi.init({ version: "2.0", sandbox: false });
      const scopes = ["payments", "username", "wallet_address"];
      const auth = await window.Pi.authenticate(scopes, (res: any) => res);
      console.log("‚úÖ X√°c th·ª±c Pi:", auth);

      // ‚úÖ 2. G·ªçi thanh to√°n
      const payment = await window.Pi.createPayment(
        {
          amount: total,
          memo: `Thanh to√°n ƒë∆°n h√†ng #${Date.now()}`,
          metadata: {
            orderId: Date.now(),
            items: cart,
            buyer: auth.user?.username || user,
          },
        },
        {
          onReadyForServerApproval: async (paymentId: string) => {
            console.log("‚è≥ [APPROVE] ID:", paymentId);
            await fetch("/api/pi/approve", {
              method: "POST",
              headers: { "Content-Type": "application/json" },
              body: JSON.stringify({ paymentId }),
            });
          },
          onReadyForServerCompletion: async (paymentId: string, txid: string) => {
            console.log("‚úÖ [COMPLETE] ID:", paymentId, txid);
            await fetch("/api/pi/complete", {
              method: "POST",
              headers: { "Content-Type": "application/json" },
              body: JSON.stringify({ paymentId, txid }),
            });
          },
          onCancel: () => alert("‚ùå Giao d·ªãch ƒë√£ b·ªã hu·ª∑."),
          onError: (err: any) => console.error("üí• L·ªói Pi SDK:", err),
        }
      );

      console.log("üí∞ K·∫øt qu·∫£ thanh to√°n:", payment);

      // ‚úÖ 3. L∆∞u ƒë∆°n h√†ng
      const order = {
        id: Date.now(),
        items: cart,
        total,
        createdAt: new Date().toISOString(),
        buyer: auth.user?.username || user,
        status: "Ch·ªù x√°c nh·∫≠n",
        note: "Thanh to√°n b·∫±ng Pi (testnet)",
      };

      await fetch("/api/orders", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(order),
      });

      clearCart();
      alert("‚úÖ Thanh to√°n qua Pi Wallet th√†nh c√¥ng!");
      router.push("/customer/pending");
    } catch (err) {
      console.error("‚ùå L·ªói thanh to√°n:", err);
      alert("‚ùå Giao d·ªãch th·∫•t b·∫°i ho·∫∑c b·ªã hu·ª∑.");
    } finally {
      setLoading(false);
    }
  };

  return (
    <main className="max-w-3xl mx-auto p-6 bg-gray-50 min-h-screen">
      <h1 className="text-2xl font-bold mb-4 text-center text-orange-600">
        üí≥ Thanh to√°n
      </h1>

      <div className="bg-white p-4 rounded shadow mb-4">
        <p>
          Ng∆∞·ªùi mua: <b>{user}</b>
        </p>
        <p>
          V√≠ Pi hi·ªán t·∫°i: <b className="text-yellow-600">{wallet} Pi</b>
        </p>
        <p>
          T·ªïng ƒë∆°n h√†ng: <b className="text-yellow-600">{total} Pi</b>
        </p>
      </div>

      <button
        onClick={handlePayWithPi}
        disabled={loading}
        className={`w-full py-3 rounded text-white font-semibold ${
          loading ? "bg-gray-400" : "bg-purple-600 hover:bg-purple-700"
        }`}
      >
        {loading ? "ƒêang m·ªü Pi Wallet..." : "Thanh to√°n b·∫±ng Pi Wallet (Testnet)"}
      </button>
    </main>
  );
}
