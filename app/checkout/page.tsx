"use client";

import { useEffect, useState } from "react";
import { useCart } from "../context/CartContext";
import { useRouter } from "next/navigation";
import { ArrowLeft } from "lucide-react";

declare global {
  interface Window {
    Pi?: any;
    Pi_initialized?: boolean;
  }
}

export default function CheckoutPage() {
  const { cart, clearCart, total } = useCart();
  const router = useRouter();
  const [loading, setLoading] = useState(false);
  const [user, setUser] = useState<any>(null);
  const [shipping, setShipping] = useState<any>(null);

  // ‚úÖ L·∫•y th√¥ng tin user (ƒë√£ ƒëƒÉng nh·∫≠p Pi)
  useEffect(() => {
    try {
      const saved = localStorage.getItem("pi_user");
      if (saved) setUser(JSON.parse(saved));
    } catch (e) {
      console.error("‚ùå L·ªói ƒë·ªçc user:", e);
    }
  }, []);

  // ‚úÖ L·∫•y ƒë·ªãa ch·ªâ giao h√†ng
  useEffect(() => {
    const saved = localStorage.getItem("shipping_info");
    if (saved) setShipping(JSON.parse(saved));
  }, []);

  // üí∞ X·ª≠ l√Ω thanh to√°n Pi
  const handlePay = async () => {
    if (!window.Pi) {
      alert("‚ö†Ô∏è Vui l√≤ng m·ªü b·∫±ng Pi Browser ƒë·ªÉ thanh to√°n!");
      return;
    }
    if (!user) {
      alert("üîë B·∫°n c·∫ßn ƒëƒÉng nh·∫≠p Pi Network tr∆∞·ªõc khi thanh to√°n!");
      router.push("/pilogin");
      return;
    }
    if (!shipping) {
      alert("üì¶ Vui l√≤ng th√™m ƒë·ªãa ch·ªâ giao h√†ng!");
      router.push("/customer/address");
      return;
    }
    if (cart.length === 0) {
      alert("üõí Gi·ªè h√†ng tr·ªëng!");
      return;
    }

    setLoading(true);
    try {
      // ‚úÖ Ch·ªâ kh·ªüi t·∫°o Pi SDK n·∫øu ch∆∞a c√≥
      if (!window.Pi_initialized) {
        const isSandbox =
          process.env.NEXT_PUBLIC_PI_ENV === "sandbox" ||
          process.env.NEXT_PUBLIC_PI_API_URL?.includes("/sandbox");
        await window.Pi.init({ version: "2.0", sandbox: isSandbox });
        window.Pi_initialized = true;
        console.log("‚úÖ Pi SDK initialized (checkout)");
      }

      const orderId = `ORD-${Date.now()}`;
      const paymentData = {
        amount: total.toFixed(2),
        memo: `Thanh to√°n ƒë∆°n h√†ng #${orderId}`,
        metadata: {
          orderId,
          buyer: user.username,
          uid: user.uid,
          cart,
        },
      };

      // ‚úÖ G·ªçi backend t·∫°o payment
      const createRes = await fetch("/api/pi/create", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(paymentData),
      });

      const payment = await createRes.json();
      console.log("üì¶ [Pi CREATE RESULT]:", payment);

      if (!payment.identifier && !payment.id) {
        throw new Error("Kh√¥ng th·ªÉ t·∫°o giao d·ªãch tr√™n Pi Network!");
      }

      // ‚úÖ Callback x·ª≠ l√Ω c√°c b∆∞·ªõc thanh to√°n
      const callbacks = {
        onReadyForServerApproval: async (paymentId: string) => {
          console.log("‚è≥ [APPROVE]", paymentId);
          await fetch("/api/pi/approve", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ paymentId }),
          });
        },

        onReadyForServerCompletion: async (paymentId: string, txid: string) => {
          console.log("‚úÖ [COMPLETE]", paymentId, txid);

          // L∆∞u ƒë∆°n h√†ng
          await fetch("/api/orders", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({
              id: orderId,
              buyer: user.username,
              total,
              items: cart,
              txid,
              shipping,
              status: "ƒê√£ thanh to√°n",
              createdAt: new Date().toISOString(),
            }),
          });

          // Ho√†n t·∫•t giao d·ªãch
          await fetch("/api/pi/complete", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ paymentId, txid }),
          });

          clearCart();
          alert("üéâ Thanh to√°n th√†nh c√¥ng!");
          router.push("/customer/pending");
        },

        onCancel: async (paymentId: string) => {
          console.log("üõë [CANCEL]", paymentId);
          await fetch("/api/pi/cancel", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ paymentId }),
          });
          alert("‚ùå Giao d·ªãch ƒë√£ b·ªã h·ªßy!");
        },

        onError: (error: any) => {
          console.error("üí• [ERROR]", error);
          alert("‚ùå L·ªói: " + error.message);
        },
      };

      // ‚úÖ G·ªçi thanh to√°n b·∫±ng Pi SDK
      await window.Pi.createPayment(paymentData, callbacks);
    } catch (err: any) {
      console.error("üí• Giao d·ªãch th·∫•t b·∫°i:", err);
      alert("‚ùå Thanh to√°n th·∫•t b·∫°i: " + err.message);
    } finally {
      setLoading(false);
    }
  };

  // ---------------- UI ----------------
  return (
    <main className="max-w-md mx-auto min-h-screen bg-gray-50 flex flex-col justify-between">
      {/* Thanh ƒëi·ªÅu h∆∞·ªõng */}
      <div className="flex items-center justify-between bg-white p-3 border-b sticky top-0 z-10">
        <button
          onClick={() => router.back()}
          className="flex items-center text-gray-700 hover:text-blue-600"
        >
          <ArrowLeft className="w-5 h-5 mr-1" />
          <span>Back</span>
        </button>
        <h1 className="font-semibold text-gray-800">Thanh to√°n</h1>
        <div className="w-5" />
      </div>

      {/* Th√¥ng tin */}
      <div className="flex-1 overflow-y-auto pb-28">
        {/* ƒê·ªãa ch·ªâ */}
        <div
          className="bg-white border-b border-gray-200 p-4 flex justify-between items-center cursor-pointer"
          onClick={() => router.push("/customer/address")}
        >
          {shipping ? (
            <div className="flex-1">
              <p className="font-semibold text-gray-800">{shipping.name}</p>
              <p className="text-gray-600 text-sm">{shipping.phone}</p>
              <p className="text-gray-500 text-sm">
                {shipping.country}, {shipping.address}
              </p>
            </div>
          ) : (
            <p className="text-gray-500">‚ûï Th√™m ƒë·ªãa ch·ªâ giao h√†ng</p>
          )}
          <span className="text-blue-500 text-sm ml-3">Ch·ªânh s·ª≠a ‚ûú</span>
        </div>

        {/* Gi·ªè h√†ng */}
        <div className="p-4 bg-white mt-2 border-t">
          <h2 className="font-semibold text-gray-800 mb-2">Gi·ªè h√†ng</h2>
          {cart.length === 0 ? (
            <p className="text-gray-500 text-sm">Kh√¥ng c√≥ s·∫£n ph·∫©m n√†o.</p>
          ) : (
            <div className="space-y-3">
              {cart.map((item, i) => (
                <div key={i} className="flex items-center border-b pb-2">
                  <img
                    src={
                      item.image?.startsWith("http")
                        ? item.image
                        : `https://muasam.titi.onl${item.image || ""}`
                    }
                    alt={item.name}
                    className="w-16 h-16 object-cover rounded border bg-gray-100"
                  />
                  <div className="ml-3 flex-1">
                    <p className="text-gray-800 font-medium text-sm">{item.name}</p>
                    <p className="text-gray-500 text-xs">
                      x{item.quantity} √ó {item.price} Pi
                    </p>
                  </div>
                  <p className="text-orange-600 font-semibold text-sm">
                    {(item.price * item.quantity).toFixed(2)} Pi
                  </p>
                </div>
              ))}
            </div>
          )}
        </div>
      </div>

      {/* N√∫t thanh to√°n */}
      <div className="fixed bottom-16 left-0 right-0 bg-white border-t p-4 flex justify-between items-center max-w-md mx-auto">
        <div>
          <p className="text-gray-600 text-sm">T·ªïng c·ªông:</p>
          <p className="text-xl font-bold text-orange-600">
            {total.toFixed(2)} Pi
          </p>
        </div>
        <button
          onClick={handlePay}
          disabled={loading}
          className={`px-6 py-3 rounded-lg font-semibold text-white text-sm ${
            loading
              ? "bg-gray-400"
              : "bg-orange-600 hover:bg-orange-700 active:bg-orange-800"
          }`}
        >
          {loading ? "ƒêang x·ª≠ l√Ω..." : "Pay Now"}
        </button>
      </div>
    </main>
  );
}
