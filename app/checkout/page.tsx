"use client";

import { useEffect, useState } from "react";
import { useCart } from "../context/CartContext";
import { useRouter } from "next/navigation";
import { ArrowLeft } from "lucide-react";

declare global {
  interface Window {
    Pi?: any;
  }
}

export default function CheckoutPage() {
  const { cart, clearCart, total } = useCart();
  const router = useRouter();
  const [loading, setLoading] = useState(false);
  const [user, setUser] = useState("guest");
  const [shipping, setShipping] = useState<any>(null);

  // ‚úÖ L·∫•y username ƒëƒÉng nh·∫≠p
  useEffect(() => {
    const username = localStorage.getItem("titi_username");
    if (username) setUser(username);
  }, []);

  // ‚úÖ L·∫•y ƒë·ªãa ch·ªâ giao h√†ng
  useEffect(() => {
    const saved = localStorage.getItem("shipping_info");
    if (saved) setShipping(JSON.parse(saved));
  }, []);

  // üí≥ Thanh to√°n
  const handlePay = async () => {
    if (!window.Pi) {
      alert("‚ö†Ô∏è Vui l√≤ng m·ªü trang n√†y trong Pi Browser ƒë·ªÉ thanh to√°n!");
      return;
    }

    if (!shipping) {
      alert("üì¶ Vui l√≤ng th√™m ƒë·ªãa ch·ªâ giao h√†ng tr∆∞·ªõc!");
      router.push("/customer/address");
      return;
    }

    if (cart.length === 0) {
      alert("üõí Gi·ªè h√†ng c·ªßa b·∫°n ƒëang tr·ªëng!");
      return;
    }

    setLoading(true);

    try {
      // üßπ Ki·ªÉm tra v√† h·ªßy giao d·ªãch pending c≈© n·∫øu c√≥
      try {
        const pendings = await window.Pi.getCurrentPayments();
        if (pendings && pendings.length > 0) {
          const last = pendings[0];
          alert(`‚ö†Ô∏è C√≥ giao d·ªãch Pi ƒëang treo (PaymentID: ${last.identifier}). H·ªá th·ªëng s·∫Ω t·ª± hu·ª∑...`);

          const cancelRes = await fetch("/api/pi/cancel", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ paymentId: last.identifier }),
          });

          const cancelText = await cancelRes.text();
          console.log("üßπ [PENDING CANCEL RESULT]:", cancelRes.status, cancelText);

          if (cancelRes.ok) {
            await new Promise((r) => setTimeout(r, 4000));
            alert("‚úÖ Giao d·ªãch c≈© ƒë√£ ƒë∆∞·ª£c hu·ª∑. B·∫Øt ƒë·∫ßu t·∫°o giao d·ªãch m·ªõi...");
          } else {
            alert("‚ö†Ô∏è Kh√¥ng th·ªÉ hu·ª∑ giao d·ªãch c≈©. Vui l√≤ng th·ª≠ l·∫°i sau 10 gi√¢y.");
            setLoading(false);
            return;
          }
        }
      } catch (err) {
        console.warn("Kh√¥ng th·ªÉ truy xu·∫•t pending payment:", err);
      }

      // üöÄ Kh·ªüi t·∫°o Pi SDK
      window.Pi.init({ version: "2.0", sandbox: false });

      const scopes = ["payments", "username", "wallet_address"];
      const auth = await window.Pi.authenticate(scopes, (res: any) => res);

      const orderId = Date.now();
      const paymentData = {
        amount: total,
        memo: `Thanh to√°n ƒë∆°n h√†ng #${orderId}`,
        metadata: {
          orderId,
          buyer: auth.user?.username || user,
          items: cart,
        },
      };

      const callbacks = {
        onReadyForServerApproval: async (paymentId: string) => {
          console.log("üü° [APPROVE] Giao d·ªãch:", paymentId);
          await fetch("/api/pi/approve", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ paymentId }),
          });
        },

        onReadyForServerCompletion: async (paymentId: string, txid: string) => {
          console.log("üü¢ [COMPLETE] Giao d·ªãch:", paymentId);

          const res = await fetch("/api/pi/complete", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ paymentId, txid }),
          });

          const result = await res.json();

          if (result?.success || res.ok) {
            const order = {
              id: orderId,
              buyer: auth.user?.username || user,
              items: cart,
              total,
              status: "ƒê√£ thanh to√°n",
              note: `Pi TXID: ${txid}`,
              shipping,
              createdAt: new Date().toISOString(),
            };

            await fetch("/api/orders", {
              method: "POST",
              headers: { "Content-Type": "application/json" },
              body: JSON.stringify(order),
            });

            clearCart();
            alert("‚úÖ Thanh to√°n th√†nh c√¥ng!");
            router.push("/customer/pending");
          } else {
            alert(result.message || "‚ö†Ô∏è Giao d·ªãch ƒëang ch·ªù x√°c minh tr√™n Pi Network.");
          }
        },

        onCancel: () => {
          alert("‚ùå B·∫°n ƒë√£ hu·ª∑ giao d·ªãch.");
        },

        onError: (error: any) => {
          console.error("üí• L·ªói thanh to√°n:", error);
          alert("üí• L·ªói thanh to√°n: " + (error.message || "Kh√¥ng x√°c ƒë·ªãnh."));
        },
      };

      await window.Pi.createPayment(paymentData, callbacks);
    } catch (err: any) {
      console.error("‚ùå L·ªói:", err);
      alert("‚ùå Thanh to√°n th·∫•t b·∫°i ho·∫∑c b·ªã hu·ª∑: " + err.message);
    } finally {
      setLoading(false);
    }
  };

  return (
    <main className="max-w-md mx-auto min-h-screen bg-gray-50 flex flex-col justify-between">
      {/* Header */}
      <div className="flex items-center justify-between bg-white p-3 border-b sticky top-0 z-10">
        <button
          onClick={() => router.back()}
          className="flex items-center text-gray-700 hover:text-blue-600"
        >
          <ArrowLeft className="w-5 h-5 mr-1" />
          <span>Back</span>
        </button>
        <h1 className="font-semibold text-gray-800">Thanh to√°n</h1>
        <div className="w-5" />
      </div>

      {/* N·ªôi dung */}
      <div className="flex-1 overflow-y-auto pb-24">
        {/* ƒê·ªãa ch·ªâ */}
        <div
          className="bg-white border-b border-gray-200 p-4 flex justify-between items-center cursor-pointer"
          onClick={() => router.push("/customer/address")}
        >
          {shipping ? (
            <div className="flex-1">
              <p className="font-semibold text-gray-800">{shipping.name}</p>
              <p className="text-gray-600 text-sm">{shipping.phone}</p>
              <p className="text-gray-500 text-sm">
                {shipping.country}, {shipping.address}
              </p>
            </div>
          ) : (
            <p className="text-gray-500">‚ûï Th√™m ƒë·ªãa ch·ªâ giao h√†ng</p>
          )}
          <span className="text-blue-500 text-sm ml-3">Ch·ªânh s·ª≠a ‚ûú</span>
        </div>

        {/* Gi·ªè h√†ng */}
        <div className="p-4 bg-white mt-2 border-t">
          <h2 className="font-semibold text-gray-800 mb-2">S·∫£n ph·∫©m</h2>
          {cart.length === 0 ? (
            <p className="text-gray-500 text-sm">Kh√¥ng c√≥ s·∫£n ph·∫©m n√†o.</p>
          ) : (
            <div className="space-y-3">
              {cart.map((item, i) => (
                <div
                  key={i}
                  className="flex items-center border-b border-gray-100 pb-2 hover:bg-gray-50 rounded"
                  onClick={() => router.push(`/product/${item.id}`)}
                >
                  <img
                    src={item.image || "/placeholder.png"}
                    alt={item.name}
                    className="w-16 h-16 object-cover rounded border"
                    onError={(e) => {
                      (e.target as HTMLImageElement).src = "/placeholder.png";
                    }}
                  />
                  <div className="ml-3 flex-1">
                    <p className="text-gray-800 font-medium text-sm">{item.name}</p>
                    <p className="text-gray-500 text-xs">
                      x{item.quantity} √ó {item.price} Pi
                    </p>
                  </div>
                  <p className="text-orange-600 font-semibold text-sm">
                    {(item.price * item.quantity).toFixed(2)} Pi
                  </p>
                </div>
              ))}
            </div>
          )}
        </div>
      </div>

      {/* Thanh t·ªïng c·ªông */}
      <div className="fixed bottom-16 left-0 right-0 bg-white border-t border-gray-200 p-4 flex justify-between items-center max-w-md mx-auto">
        <div>
          <p className="text-gray-600 text-sm">T·ªïng c·ªông:</p>
          <p className="text-xl font-bold text-orange-600">{total.toFixed(2)} Pi</p>
        </div>
        <button
          onClick={handlePay}
          disabled={loading}
          className={`px-6 py-3 rounded-lg font-semibold text-white text-sm ${
            loading ? "bg-gray-400" : "bg-orange-600 hover:bg-orange-700 active:bg-orange-800"
          }`}
        >
          {loading ? "ƒêang x·ª≠ l√Ω..." : "Pay Now"}
        </button>
      </div>
    </main>
  );
}
