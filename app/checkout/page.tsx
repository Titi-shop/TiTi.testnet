"use client";

import { useEffect, useState } from "react";
import { useCart } from "../context/CartContext";
import { useRouter } from "next/navigation";
import { ArrowLeft } from "lucide-react";

declare global {
  interface Window {
    Pi?: any;
  }
}

export default function CheckoutPage() {
  const { cart, clearCart, total } = useCart();
  const router = useRouter();
  const [loading, setLoading] = useState(false);
  const [user, setUser] = useState("guest");
  const [shipping, setShipping] = useState<any>(null);

  // ‚úÖ L·∫•y th√¥ng tin ng∆∞·ªùi d√πng
  useEffect(() => {
    try {
      const username = localStorage.getItem("titi_username");
      if (username) setUser(username);
    } catch {}
  }, []);

  // ‚úÖ L·∫•y ƒë·ªãa ch·ªâ giao h√†ng
  useEffect(() => {
    const saved = localStorage.getItem("shipping_info");
    if (saved) setShipping(JSON.parse(saved));
  }, []);

  // üí∞ Thanh to√°n qua Pi
  const handlePay = async () => {
    if (!window.Pi) {
      alert("‚ö†Ô∏è H√£y m·ªü trong Pi Browser ƒë·ªÉ thanh to√°n!");
      return;
    }
    if (!shipping) {
      alert("üì¶ Vui l√≤ng ch·ªçn ƒë·ªãa ch·ªâ giao h√†ng!");
      router.push("/customer/address");
      return;
    }
    if (cart.length === 0) {
      alert("üõí Gi·ªè h√†ng tr·ªëng!");
      return;
    }

    setLoading(true);

    try {
      window.Pi.init({ version: "2.0", sandbox: false });
      const scopes = ["payments", "username", "wallet_address"];
      const auth = await window.Pi.authenticate(scopes, (res: any) => res);

      const orderId = Date.now();
      const paymentData = {
        amount: total,
        memo: `Thanh to√°n ƒë∆°n h√†ng #${orderId}`,
        metadata: {
          orderId,
          buyer: auth.user?.username || user,
          items: cart,
        },
      };

      // üî• C√°c callback t·ª´ Pi SDK
      const callbacks = {
        // ‚ùå Kh√¥ng l∆∞u ƒë∆°n h√†ng ·ªü ƒë√¢y ‚Äî ch·ªâ x√°c nh·∫≠n chu·∫©n b·ªã thanh to√°n
        onReadyForServerApproval: async (paymentId: string) => {
          console.log("üì© Ch·ªù x√°c nh·∫≠n server:", paymentId);
        },

        // ‚úÖ Khi ng∆∞·ªùi d√πng thanh to√°n th√†nh c√¥ng trong v√≠ Pi
        onReadyForServerCompletion: async (paymentId: string, txid: string) => {
          console.log("‚úÖ Pi thanh to√°n th√†nh c√¥ng:", { paymentId, txid });

          // G·ªçi server ƒë·ªÉ x√°c minh Pi transaction
          const verifyRes = await fetch("/api/pi/complete", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ paymentId, txid }),
          });
          const verifyData = await verifyRes.json();

          if (verifyData?.success) {
            // ‚úÖ Ch·ªâ l∆∞u ƒë∆°n khi Pi x√°c nh·∫≠n h·ª£p l·ªá
            const order = {
              id: orderId,
              buyer: auth.user?.username || user,
              items: cart,
              total,
              status: "ƒê√£ thanh to√°n",
              note: `Pi TXID: ${txid}`,
              createdAt: new Date().toISOString(),
              shipping,
            };

            await fetch("/api/orders", {
              method: "POST",
              headers: { "Content-Type": "application/json" },
              body: JSON.stringify(order),
            });

            clearCart();
            alert("‚úÖ Thanh to√°n th√†nh c√¥ng!");
            router.push("/customer/pending");
          } else {
            alert("‚ö†Ô∏è Giao d·ªãch ch∆∞a ƒë∆∞·ª£c x√°c minh. Vui l√≤ng th·ª≠ l·∫°i!");
          }
        },

        onCancel: () => {
          console.warn("‚ùå Ng∆∞·ªùi d√πng ƒë√£ hu·ª∑ giao d·ªãch.");
          alert("Giao d·ªãch b·ªã hu·ª∑.");
        },

        onError: (error: any) => {
          console.error("üí• L·ªói thanh to√°n:", error);
          alert("L·ªói trong qu√° tr√¨nh thanh to√°n: " + error.message);
        },
      };

      await window.Pi.createPayment(paymentData, callbacks);
    } catch (err: any) {
      console.error("‚ùå L·ªói thanh to√°n:", err);
      alert("‚ùå Giao d·ªãch th·∫•t b·∫°i ho·∫∑c b·ªã hu·ª∑.");
    } finally {
      setLoading(false);
    }
  };

  // ‚úÖ Chu·∫©n ho√° link ·∫£nh t·ª´ Vercel (n·∫øu ch∆∞a c√≥ domain)
  const getImageUrl = (url: string) => {
    if (!url) return "/placeholder.png";
    if (url.startsWith("http")) return url;
    return `https://muasam-titi.vercel.app${url}`;
  };

  return (
    <main className="max-w-md mx-auto min-h-screen bg-gray-50 flex flex-col justify-between">
      {/* üîπ Thanh ƒëi·ªÅu h∆∞·ªõng */}
      <div className="flex items-center justify-between bg-white p-3 border-b sticky top-0 z-10">
        <button
          onClick={() => router.back()}
          className="flex items-center text-gray-700 hover:text-blue-600"
        >
          <ArrowLeft className="w-5 h-5 mr-1" />
          <span>Back</span>
        </button>
        <h1 className="font-semibold text-gray-800">Thanh to√°n</h1>
        <div className="w-5" />
      </div>

      {/* üîπ N·ªôi dung ch√≠nh */}
      <div className="flex-1 overflow-y-auto pb-24">
        {/* ƒê·ªãa ch·ªâ giao h√†ng */}
        <div
          className="bg-white border-b border-gray-200 p-4 flex justify-between items-center cursor-pointer"
          onClick={() => router.push("/customer/address")}
        >
          {shipping ? (
            <div className="flex-1">
              <p className="font-semibold text-gray-800">{shipping.name}</p>
              <p className="text-gray-600 text-sm">{shipping.phone}</p>
              <p className="text-gray-500 text-sm">
                {shipping.country}, {shipping.address}
              </p>
            </div>
          ) : (
            <p className="text-gray-500">‚ûï Th√™m ƒë·ªãa ch·ªâ giao h√†ng</p>
          )}
          <span className="text-blue-500 text-sm ml-3">Ch·ªânh s·ª≠a ‚ûú</span>
        </div>

        {/* Gi·ªè h√†ng */}
        <div className="p-4 bg-white mt-2 border-t">
          <h2 className="font-semibold text-gray-800 mb-2">S·∫£n ph·∫©m</h2>
          {cart.length === 0 ? (
            <p className="text-gray-500 text-sm">Kh√¥ng c√≥ s·∫£n ph·∫©m n√†o.</p>
          ) : (
            <div className="space-y-3">
              {cart.map((item, i) => (
                <div
                  key={i}
                  className="flex items-center border-b border-gray-100 pb-2 cursor-pointer hover:bg-gray-50 rounded"
                  onClick={() => router.push(`/product/${item.id}`)}
                >
                  <img
                    src={getImageUrl(item.image)}
                    alt={item.name}
                    className="w-16 h-16 object-cover rounded border"
                    onError={(e) =>
                      ((e.target as HTMLImageElement).src = "/placeholder.png")
                    }
                  />
                  <div className="ml-3 flex-1">
                    <p className="text-gray-800 font-medium text-sm">
                      {item.name}
                    </p>
                    <p className="text-gray-500 text-xs">
                      x{item.quantity} √ó {item.price} Pi
                    </p>
                  </div>
                  <p className="text-orange-600 font-semibold text-sm">
                    {(item.price * item.quantity).toFixed(2)} Pi
                  </p>
                </div>
              ))}
            </div>
          )}
        </div>
      </div>

      {/* üîπ Thanh t·ªïng c·ªông + n√∫t thanh to√°n */}
      <div className="fixed bottom-16 left-0 right-0 bg-white border-t border-gray-200 p-4 flex justify-between items-center max-w-md mx-auto">
        <div>
          <p className="text-gray-600 text-sm">T·ªïng c·ªông:</p>
          <p className="text-xl font-bold text-orange-600">
            {total.toFixed(2)} Pi
          </p>
        </div>
        <button
          onClick={handlePay}
          disabled={loading}
          className={`px-6 py-3 rounded-lg font-semibold text-white text-sm ${
            loading
              ? "bg-gray-400"
              : "bg-orange-600 hover:bg-orange-700 active:bg-orange-800"
          }`}
        >
          {loading ? "ƒêang x·ª≠ l√Ω..." : "Pay Now"}
        </button>
      </div>
    </main>
  );
}
