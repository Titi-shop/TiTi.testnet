"use client";

import React, { useEffect, useState } from "react";

export default function CheckoutPage() {
  const [user, setUser] = useState<{ username: string } | null>(null);
  const [product, setProduct] = useState<any>(null);
  const [isPaying, setIsPaying] = useState(false);

  const [country, setCountry] = useState("");
  const [address, setAddress] = useState("");
  const [phone, setPhone] = useState("");

  // üîπ L·∫•y th√¥ng tin ƒëƒÉng nh·∫≠p Pi
  useEffect(() => {
    const stored = localStorage.getItem("pi_user");
    if (stored) {
      try {
        const parsed = JSON.parse(stored);
        setUser(parsed.user);
      } catch {
        setUser(null);
      }
    }
  }, []);

  // üîπ L·∫•y d·ªØ li·ªáu s·∫£n ph·∫©m t·ª´ API
  useEffect(() => {
    const fetchProduct = async () => {
      try {
        const res = await fetch("/api/products");
        const data = await res.json();
        // L·∫•y s·∫£n ph·∫©m ƒë·∫ßu ti√™n l√†m v√≠ d·ª•
        setProduct(data[0]);
      } catch (error) {
        console.error("‚ùå L·ªói khi t·∫£i s·∫£n ph·∫©m:", error);
      }
    };
    fetchProduct();
  }, []);

  // üîπ Thanh to√°n qua Pi Testnet
  const handlePayment = async () => {
    if (typeof window === "undefined" || !window.Pi) {
      alert("‚ö†Ô∏è Vui l√≤ng m·ªü trong Pi Browser ƒë·ªÉ thanh to√°n!");
      return;
    }
    if (!user) {
      alert("‚ö†Ô∏è B·∫°n c·∫ßn ƒëƒÉng nh·∫≠p b·∫±ng Pi tr∆∞·ªõc!");
      window.location.href = "/pilogin";
      return;
    }
    if (!country || !address || !phone) {
      alert("‚ö†Ô∏è Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß th√¥ng tin giao h√†ng!");
      return;
    }

    setIsPaying(true);
    try {
      window.Pi.init({ version: "2.0", sandbox: true });

      const paymentData = {
        amount: product?.price || 0.5,
        memo: `Thanh to√°n ${product?.name}`,
        metadata: { buyer: user.username, country, address, phone },
      };

      const callbacks = {
        onReadyForServerApproval: (paymentId: string) => {
          console.log("‚úÖ Ready for approval:", paymentId);
        },
        onReadyForServerCompletion: (paymentId: string, txid: string) => {
          console.log("‚úÖ Payment completed:", paymentId, txid);
          window.open(`https://wallet-testnet.minepi.com/transaction/${txid}`, "_blank");
        },
        onCancel: (paymentId: string) => {
          console.log("‚ùå Payment cancelled:", paymentId);
          setIsPaying(false);
        },
        onError: (error: any) => {
          console.error("‚ùå Payment error:", error);
          setIsPaying(false);
        },
      };

      await window.Pi.createPayment(paymentData, callbacks);
    } catch (err) {
      console.error("Payment failed:", err);
    } finally {
      setIsPaying(false);
    }
  };

  return (
    <main className="min-h-screen bg-gray-100 flex justify-center py-10 px-4">
      <div className="bg-white w-full max-w-md rounded-2xl shadow-lg p-6 space-y-6">
        <h1 className="text-2xl font-semibold text-center">Checkout</h1>

        {user ? (
          <p className="text-center text-gray-600">
            üëã Xin ch√†o <strong>{user.username}</strong>
          </p>
        ) : (
          <p className="text-center text-red-500">‚ö†Ô∏è B·∫°n ch∆∞a ƒëƒÉng nh·∫≠p Pi</p>
        )}

        {product ? (
          <div className="flex items-center gap-4 border p-3 rounded-xl">
            <img
              src={product.images?.[0]}
              alt={product.name}
              className="w-20 h-20 rounded-lg object-cover"
            />
            <div>
              <h2 className="font-semibold">{product.name}</h2>
              <p className="text-gray-600">Price: œÄ{product.price}</p>
            </div>
          </div>
        ) : (
          <p className="text-center text-gray-400">ƒêang t·∫£i s·∫£n ph·∫©m...</p>
        )}

        <div className="space-y-3">
          <h3 className="font-semibold text-lg">Th√¥ng tin giao h√†ng</h3>
          <input
            type="text"
            placeholder="Qu·ªëc gia"
            value={country}
            onChange={(e) => setCountry(e.target.value)}
            className="w-full border border-gray-300 rounded-lg px-3 py-2 focus:ring-2 focus:ring-orange-500"
          />
          <input
            type="text"
            placeholder="ƒê·ªãa ch·ªâ nh·∫≠n h√†ng"
            value={address}
            onChange={(e) => setAddress(e.target.value)}
            className="w-full border border-gray-300 rounded-lg px-3 py-2 focus:ring-2 focus:ring-orange-500"
          />
          <input
            type="tel"
            placeholder="S·ªë ƒëi·ªán tho·∫°i"
            value={phone}
            onChange={(e) => setPhone(e.target.value)}
            className="w-full border border-gray-300 rounded-lg px-3 py-2 focus:ring-2 focus:ring-orange-500"
          />
        </div>

        <div className="border-t border-gray-200"></div>

        {product && (
          <div className="flex justify-between text-lg font-semibold">
            <span>Total:</span>
            <span>œÄ{(product.price * 0.97).toFixed(2)}</span>
          </div>
        )}

        <button
          onClick={handlePayment}
          disabled={isPaying || !product}
          className="w-full bg-orange-500 text-white py-3 rounded-xl font-medium hover:bg-orange-600 disabled:opacity-50"
        >
          {isPaying ? "Processing..." : "Pay with Pi (Testnet)"}
        </button>
      </div>
    </main>
  );
}
