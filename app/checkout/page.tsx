"use client";

import { useEffect, useState } from "react";
import { useCart } from "../context/CartContext";
import { useRouter } from "next/navigation";

declare global {
  interface Window {
    Pi?: any;
  }
}

export default function CheckoutPage() {
  const { cart, clearCart, total } = useCart();
  const router = useRouter();
  const [wallet, setWallet] = useState<number>(0);
  const [loading, setLoading] = useState(false);
  const [user, setUser] = useState("guest");
  const [shipping, setShipping] = useState<any>(null);

  // ‚úÖ L·∫•y th√¥ng tin ƒëƒÉng nh·∫≠p
  useEffect(() => {
    try {
      const username = localStorage.getItem("titi_username");
      if (username) setUser(username);
    } catch {}
  }, []);

  // ‚úÖ L·∫•y v√≠ Pi (mock)
  useEffect(() => {
    const w = Number(localStorage.getItem("pi_wallet") ?? "1000");
    setWallet(w);
  }, []);

  // ‚úÖ L·∫•y ƒë·ªãa ch·ªâ giao h√†ng ƒë√£ l∆∞u
  useEffect(() => {
    const saved = localStorage.getItem("shipping_info");
    if (saved) setShipping(JSON.parse(saved));
  }, []);

  // üí∞ Thanh to√°n qua Pi
  const handlePay = async () => {
    if (!window.Pi) {
      alert("‚ö†Ô∏è H√£y m·ªü trong Pi Browser ƒë·ªÉ thanh to√°n!");
      return;
    }
    if (!shipping) {
      alert("üì¶ Vui l√≤ng ch·ªçn ƒë·ªãa ch·ªâ giao h√†ng!");
      router.push("/customer/address");
      return;
    }
    if (cart.length === 0) {
      alert("üõí Gi·ªè h√†ng tr·ªëng!");
      return;
    }

    setLoading(true);

    try {
      window.Pi.init({ version: "2.0", sandbox: false });
      const scopes = ["payments", "username", "wallet_address"];
      const auth = await window.Pi.authenticate(scopes, (res: any) => res);

      const orderId = Date.now();
      const paymentData = {
        amount: total,
        memo: `Thanh to√°n ƒë∆°n h√†ng #${orderId}`,
        metadata: {
          orderId,
          items: cart,
          buyer: auth.user?.username || user,
        },
      };

      const callbacks = {
        onReadyForServerApproval: async (paymentId: string) => {
          await fetch("/api/pi/approve", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ paymentId }),
          });
        },
        onReadyForServerCompletion: async (paymentId: string, txid: string) => {
          const res = await fetch("/api/pi/complete", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ paymentId, txid }),
          });
          const result = await res.json();

          if (result?.success) {
            const order = {
              id: orderId,
              items: cart,
              total,
              buyer: auth.user?.username || user,
              status: "ƒê√£ thanh to√°n",
              note: `Pi TXID: ${txid}`,
              createdAt: new Date().toISOString(),
              shipping,
            };

            await fetch("/api/orders", {
              method: "POST",
              headers: { "Content-Type": "application/json" },
              body: JSON.stringify(order),
            });

            clearCart();
            alert("‚úÖ Thanh to√°n th√†nh c√¥ng!");
            router.push("/customer/pending");
          } else {
            alert("‚ö†Ô∏è Giao d·ªãch ƒëang ch·ªù x√°c minh.");
          }
        },
        onCancel: () => alert("‚ùå Giao d·ªãch b·ªã hu·ª∑."),
        onError: (error: any) => {
          console.error("üí• L·ªói:", error);
          alert("üí• L·ªói trong qu√° tr√¨nh thanh to√°n: " + error.message);
        },
      };

      await window.Pi.createPayment(paymentData, callbacks);
    } catch (err: any) {
      console.error("‚ùå L·ªói thanh to√°n:", err);
      alert("‚ùå Giao d·ªãch th·∫•t b·∫°i ho·∫∑c b·ªã hu·ª∑.");
    } finally {
      setLoading(false);
    }
  };

  // üß± Giao di·ªán
  return (
    <main className="max-w-md mx-auto min-h-screen bg-gray-50 flex flex-col justify-between">
      <div>
        {/* ƒê·ªãa ch·ªâ giao h√†ng */}
        <div
          className="bg-white border-b border-gray-200 p-4 flex justify-between items-center cursor-pointer"
          onClick={() => router.push("/customer/address")}
        >
          {shipping ? (
            <div className="flex-1">
              <p className="font-semibold text-gray-800">{shipping.name}</p>
              <p className="text-gray-600 text-sm">{shipping.phone}</p>
              <p className="text-gray-500 text-sm">
                {shipping.country}, {shipping.address}
              </p>
            </div>
          ) : (
            <p className="text-gray-500">‚ûï Th√™m ƒë·ªãa ch·ªâ giao h√†ng</p>
          )}
          <span className="text-blue-500 text-sm ml-3">Ch·ªânh s·ª≠a ‚ûú</span>
        </div>

        {/* Danh s√°ch s·∫£n ph·∫©m */}
        <div className="p-4 bg-white mt-2 border-t">
          <h2 className="font-semibold text-gray-800 mb-2">üõí Gi·ªè h√†ng</h2>
          {cart.length === 0 ? (
            <p className="text-gray-500 text-sm">Kh√¥ng c√≥ s·∫£n ph·∫©m n√†o.</p>
          ) : (
            <div className="space-y-3">
              {cart.map((item, i) => (
                <div
                  key={i}
                  className="flex items-center border-b border-gray-100 pb-2 cursor-pointer hover:bg-gray-50 rounded"
                  onClick={() => router.push(`/product/${item.id}`)}
                >
                  <img
                    src={item.image || "/placeholder.png"}
                    alt={item.name}
                    className="w-16 h-16 object-cover rounded"
                  />
                  <div className="ml-3 flex-1">
                    <p className="text-gray-800 font-medium text-sm">
                      {item.name}
                    </p>
                    <p className="text-gray-500 text-xs">
                      x{item.quantity} √ó {item.price} Pi
                    </p>
                  </div>
                  <p className="text-orange-600 font-semibold text-sm">
                    {(item.price * item.quantity).toFixed(2)} Pi
                  </p>
                </div>
              ))}
            </div>
          )}
        </div>
      </div>

      {/* T·ªïng c·ªông + n√∫t Pay */}
      <div className="bg-white border-t border-gray-200 p-4 flex justify-between items-center">
        <div>
          <p className="text-gray-600 text-sm">T·ªïng c·ªông:</p>
          <p className="text-xl font-bold text-orange-600">{total.toFixed(2)} Pi</p>
        </div>
        <button
          onClick={handlePay}
          disabled={loading}
          className={`px-6 py-3 rounded-lg font-semibold text-white text-sm ${
            loading
              ? "bg-gray-400"
              : "bg-orange-600 hover:bg-orange-700 active:bg-orange-800"
          }`}
        >
          {loading ? "ƒêang x·ª≠ l√Ω..." : "Pay Now"}
        </button>
      </div>
    </main>
  );
}
