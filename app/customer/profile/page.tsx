"use client";

import { useState, useEffect } from "react";
import { useRouter } from "next/navigation";

export default function ProfilePage() {
  const router = useRouter();
  const [info, setInfo] = useState({
    username: "",
    email: "",
    phone: "",
    address: "",
  });
  const [editing, setEditing] = useState(false);
  const [saving, setSaving] = useState(false);
  const [lang, setLang] = useState("vi");

  useEffect(() => {
    const stored = localStorage.getItem("user_info");
    if (stored) {
      const parsed = JSON.parse(stored);
      setInfo({
        username: parsed.username || "",
        email: parsed.email || "",
        phone: parsed.phone || "",
        address: parsed.address || "",
      });
      // L·∫•y h·ªì s∆° t·ª´ server n·∫øu c√≥
      fetch(`/api/profile?username=${parsed.username}`)
        .then((res) => res.json())
        .then((data) => {
          if (data && data.username) setInfo(data);
        });
    }
  }, []);

  const handleSave = async () => {
    setSaving(true);
    const res = await fetch("/api/profile", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(info),
    });
    const data = await res.json();
    setSaving(false);
    if (data.success) {
      alert("‚úÖ H·ªì s∆° ƒë√£ l∆∞u th√†nh c√¥ng!");
      setEditing(false);
      localStorage.setItem("user_info", JSON.stringify(info));
    } else alert("‚ùå L∆∞u th·∫•t b·∫°i!");
  };

  const toggleLang = () => {
    const next = lang === "vi" ? "zh" : "vi";
    setLang(next);
    router.push(`/${next}/customer/profile`);
  };

  return (
    <main className="p-4 max-w-md mx-auto">
      <div className="flex justify-between items-center mb-4">
        <h1 className="text-xl font-bold text-orange-600">üë§ H·ªì s∆° c√° nh√¢n</h1>
        <button
          onClick={toggleLang}
          className="text-sm bg-gray-200 hover:bg-gray-300 rounded px-2 py-1"
        >
          üåê {lang === "vi" ? "‰∏≠Êñá" : "Ti·∫øng Vi·ªát"}
        </button>
      </div>

      <div className="space-y-4">
        {["username", "email", "phone", "address"].map((field) => (
          <div key={field}>
            <label className="block text-sm text-gray-700 capitalize">
              {field === "username"
                ? "T√™n ng∆∞·ªùi d√πng"
                : field === "email"
                ? "Email"
                : field === "phone"
                ? "S·ªë ƒëi·ªán tho·∫°i"
                : "ƒê·ªãa ch·ªâ"}
            </label>
            {field === "address" ? (
              <textarea
                value={info[field]}
                readOnly={!editing}
                onChange={(e) =>
                  setInfo({ ...info, [field]: e.target.value })
                }
                className={`w-full border px-3 py-2 rounded h-20 ${
                  editing ? "bg-white" : "bg-gray-100"
                }`}
              />
            ) : (
              <input
                type="text"
                value={info[field]}
                readOnly={!editing}
                onChange={(e) =>
                  setInfo({ ...info, [field]: e.target.value })
                }
                className={`w-full border px-3 py-2 rounded ${
                  editing ? "bg-white" : "bg-gray-100"
                }`}
              />
            )}
          </div>
        ))}
      </div>

      <div className="flex justify-between mt-6">
        {editing ? (
          <>
            <button
              onClick={handleSave}
              disabled={saving}
              className="bg-green-500 hover:bg-green-600 text-white px-4 py-2 rounded"
            >
              {saving ? "ƒêang l∆∞u..." : "üíæ L∆∞u"}
            </button>
            <button
              onClick={() => setEditing(false)}
              className="bg-gray-300 hover:bg-gray-400 px-4 py-2 rounded"
            >
              ‚ùå H·ªßy
            </button>
          </>
        ) : (
          <button
            onClick={() => setEditing(true)}
            className="bg-orange-500 hover:bg-orange-600 text-white px-4 py-2 rounded"
          >
            ‚úèÔ∏è Ch·ªânh s·ª≠a h·ªì s∆°
          </button>
        )}
        <button
          onClick={() => router.back()}
          className="bg-gray-300 hover:bg-gray-400 text-gray-800 px-4 py-2 rounded"
        >
          ‚Üê Quay l·∫°i
        </button>
      </div>
    </main>
  );
}
