"use client";

import { useEffect, useState } from "react";
import { useParams } from "next/navigation";
import { useCart } from "../../context/CartContext"; // ‚úÖ d√πng chung context gi·ªè h√†ng

export default function ProductDetail() {
  const { id } = useParams(); // ‚úÖ L·∫•y id t·ª´ URL
  const [product, setProduct] = useState<any>(null);
  const [loading, setLoading] = useState(true);
  const { addToCart, clearCart } = useCart();

  // ‚úÖ L·∫•y s·∫£n ph·∫©m theo ID
  useEffect(() => {
    async function fetchProduct() {
      try {
        const res = await fetch("/api/products");
        const products = await res.json();
        const found = products.find((p: any) => p.id.toString() === id.toString());
        setProduct(found);
      } catch (err) {
        console.error("‚ùå L·ªói khi t·∫£i s·∫£n ph·∫©m:", err);
      } finally {
        setLoading(false);
      }
    }

    if (id) fetchProduct();
  }, [id]);

  // ‚úÖ Th√™m s·∫£n ph·∫©m v√†o gi·ªè
  const handleAddToCart = () => {
    if (!product) return;
    addToCart({
      id: product.id,
      name: product.name,
      price: Number(product.price),
      description: product.description,
      images: product.images,
    });
    alert("‚úÖ ƒê√£ th√™m v√†o gi·ªè h√†ng!");
  };

  // ‚úÖ Thanh to√°n nhanh
  const handleCheckout = () => {
    if (!product) return;
    clearCart(); // Xo√° gi·ªè c≈©
    addToCart({
      id: product.id,
      name: product.name,
      price: Number(product.price),
      description: product.description,
      images: product.images,
    });
    window.location.href = "/checkout";
  };

  if (loading)
    return <p className="text-center mt-6">‚è≥ ƒêang t·∫£i s·∫£n ph·∫©m...</p>;

  if (!product)
    return (
      <p className="text-center mt-6 text-red-600 font-medium">
        ‚ùå Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m.
      </p>
    );

  return (
    <div className="p-6 max-w-3xl mx-auto">
      {/* üè∑Ô∏è Th√¥ng tin s·∫£n ph·∫©m */}
      <h1 className="text-2xl font-bold mb-2">{product.name}</h1>
      <p className="text-lg text-yellow-600 font-semibold mb-1">
        Gi√°: {product.price} Pi
      </p>
      <p className="text-gray-700 mb-4">
        <b>M√¥ t·∫£:</b> {product.description || "Kh√¥ng c√≥ m√¥ t·∫£."}
      </p>

      {/* üñºÔ∏è Hi·ªÉn th·ªã ·∫£nh s·∫£n ph·∫©m */}
      {product.images?.length > 0 ? (
        <div className="flex gap-3 flex-wrap mb-6">
          {product.images.map((src: string, i: number) => {
            // ‚úÖ N·∫øu ·∫£nh l√† Blob (URL https://...) th√¨ d√πng tr·ª±c ti·∫øp
            // ‚úÖ N·∫øu l√† local file (ch∆∞a upload), fallback v√†o /uploads/
            const validSrc =
              src.startsWith("http") || src.startsWith("https")
                ? src
                : `/uploads/${src.split("\\").pop()}`;

            return (
              <img
                key={i}
                src={validSrc}
                alt={`·∫¢nh ${i + 1}`}
                className="w-40 h-40 object-cover rounded border"
              />
            );
          })}
        </div>
      ) : (
        <div className="w-full aspect-square bg-gray-100 flex items-center justify-center text-gray-400 rounded-lg mb-6">
          Kh√¥ng c√≥ ·∫£nh s·∫£n ph·∫©m
        </div>
      )}

      {/* üõí N√∫t h√†nh ƒë·ªông */}
      <div className="flex gap-3 mt-6">
        <button
          onClick={handleAddToCart}
          className="bg-yellow-500 hover:bg-yellow-600 text-white px-4 py-2 rounded-lg shadow transition"
        >
          üõí Th√™m gi·ªè h√†ng
        </button>

        <button
          onClick={handleCheckout}
          className="bg-green-500 hover:bg-green-600 text-white px-4 py-2 rounded-lg shadow transition"
        >
          üí≥ Thanh to√°n
        </button>
      </div>
    </div>
  );
}
