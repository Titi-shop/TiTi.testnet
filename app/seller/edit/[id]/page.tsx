"use client";

import { useEffect, useState } from "react";
import { useRouter, useParams } from "next/navigation";
import { useLanguage } from "../../context/LanguageContext";

export default function EditProductPage() {
  const { id } = useParams();
  const router = useRouter();
  const { translate } = useLanguage();

  const [product, setProduct] = useState<any>(null);
  const [loading, setLoading] = useState(true);
  const [saving, setSaving] = useState(false);
  const [error, setError] = useState("");

  // üß© T·∫£i th√¥ng tin s·∫£n ph·∫©m theo ID
  useEffect(() => {
    if (!id) return;

    fetch("/api/products")
      .then((res) => res.json())
      .then((data) => {
        const found = data.find((p: any) => String(p.id) === String(id));
        setProduct(found || null);
        setLoading(false);
      })
      .catch((err) => {
        console.error("‚ùå L·ªói t·∫£i s·∫£n ph·∫©m:", err);
        setError("Kh√¥ng th·ªÉ t·∫£i th√¥ng tin s·∫£n ph·∫©m.");
        setLoading(false);
      });
  }, [id]);

  // üß© H√†m l∆∞u s·∫£n ph·∫©m
  async function handleSave(e: React.FormEvent) {
    e.preventDefault();
    setSaving(true);
    setError("");

    try {
      const formData = new FormData(e.target as HTMLFormElement);
      formData.append("id", String(product.id));

      const res = await fetch("/api/products", {
        method: "PUT",
        body: formData,
      });

      const result = await res.json();

      if (result.success) {
        alert("‚úÖ C·∫≠p nh·∫≠t s·∫£n ph·∫©m th√†nh c√¥ng!");
        router.push("/seller/stock");
      } else {
        setError(result.message || "L·ªói khi c·∫≠p nh·∫≠t s·∫£n ph·∫©m");
      }
    } catch (err) {
      console.error("‚ùå PUT error:", err);
      setError("Kh√¥ng th·ªÉ c·∫≠p nh·∫≠t s·∫£n ph·∫©m.");
    } finally {
      setSaving(false);
    }
  }

  if (loading)
    return <p className="text-center mt-10 text-gray-600">ƒêang t·∫£i d·ªØ li·ªáu...</p>;

  if (!product)
    return <p className="text-center mt-10 text-red-500">Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m!</p>;

  return (
    <main className="max-w-lg mx-auto p-6 bg-white rounded-xl shadow mt-10">
      <h1 className="text-xl font-bold mb-4 text-center text-gray-800">
        ‚úèÔ∏è {translate("edit_product") || "Ch·ªânh s·ª≠a s·∫£n ph·∫©m"}
      </h1>

      <form onSubmit={handleSave} className="space-y-4">
        <div>
          <label className="block font-medium text-gray-700 mb-1">T√™n s·∫£n ph·∫©m</label>
          <input
            name="name"
            defaultValue={product.name}
            required
            className="w-full border rounded-md p-2"
          />
        </div>

        <div>
          <label className="block font-medium text-gray-700 mb-1">Gi√° (Pi)</label>
          <input
            name="price"
            type="number"
            defaultValue={product.price}
            required
            className="w-full border rounded-md p-2"
          />
        </div>

        <div>
          <label className="block font-medium text-gray-700 mb-1">M√¥ t·∫£</label>
          <textarea
            name="description"
            defaultValue={product.description}
            rows={3}
            className="w-full border rounded-md p-2"
          ></textarea>
        </div>

        <div>
          <label className="block font-medium text-gray-700 mb-1">·∫¢nh (URL, c√°ch nhau b·ªüi d·∫•u ph·∫©y)</label>
          <input
            name="images"
            defaultValue={product.images?.join(", ")}
            className="w-full border rounded-md p-2"
          />
        </div>

        {error && <p className="text-red-500 text-center">{error}</p>}

        <button
          type="submit"
          disabled={saving}
          className="w-full bg-green-600 hover:bg-green-700 text-white p-3 rounded-lg font-semibold transition"
        >
          {saving ? "üíæ ƒêang l∆∞u..." : "‚úÖ L∆∞u thay ƒë·ªïi"}
        </button>
      </form>
    </main>
  );
}
